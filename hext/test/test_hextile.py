from hext.hextile import Hextile

def test_vertices():
    h = Hextile(col=1, row=1)
    assert h.vertices(100) == [
        (250.0, 259.8076211353316), 
        (200.0, 173.20508075688775), 
        (100.0, 173.20508075688775), 
        (50.0, 259.8076211353316), 
        (100.0, 346.41016151377545), 
        (200.0, 346.41016151377545)
    ]
